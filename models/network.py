class DoubleConv(nn.Module):
    """(convolution => [BN] => ReLU) * 2"""

    def __init__(self, in_channels, out_channels, mid_channels=None):
        super().__init__()
        if not mid_channels:
            mid_channels = out_channels
        self.double_conv = nn.Sequential(
            nn.Conv2d(in_channels, mid_channels, kernel_size=3, padding=1),
            nn.BatchNorm2d(mid_channels),
            nn.ReLU(inplace=True),
            nn.Conv2d(mid_channels, out_channels, kernel_size=3, padding=1),
            nn.BatchNorm2d(out_channels),
            nn.ReLU(inplace=True)
        )

    def forward(self, x):
        return self.double_conv(x)

class Up(nn.Module):
    """Upscaling then double conv inspired by https://github.com/milesial/Pytorch-UNet/blob/master/unet/unet_parts.py"""

    def __init__(self, in_channels, out_channels, bilinear=True):
        super().__init__()

        # if bilinear, use the normal convolutions to reduce the number of channels
        if bilinear:
            self.up = nn.Upsample(scale_factor=2, mode='bilinear', align_corners=True)
            self.conv = DoubleConv(in_channels, out_channels, in_channels // 2)
        else:
            self.up = nn.ConvTranspose2d(in_channels , in_channels // 2, kernel_size=2, stride=2)
            self.conv = DoubleConv(in_channels, out_channels)


    def forward(self, x1, x2):
        x1 = self.up(x1)
        # input is CHW
        diffY = x2.size()[2] - x1.size()[2]
        diffX = x2.size()[3] - x1.size()[3]

        x1 = F.pad(x1, [diffX // 2, diffX - diffX // 2,
                        diffY // 2, diffY - diffY // 2])
        x = torch.cat([x2, x1], dim=1)
        return self.conv(x)

class UpwithoutConv(nn.Module):
    """Upscaling then double conv"""

    def __init__(self, in_channels, out_channels, bilinear=True):
        super().__init__()

        # if bilinear, use the normal convolutions to reduce the number of channels
        if bilinear:
            self.up = nn.Upsample(scale_factor=2, mode='bilinear', align_corners=True)
            self.conv = DoubleConv(in_channels, out_channels, in_channels // 2)
        else:
            self.up = nn.ConvTranspose2d(in_channels , in_channels // 2, kernel_size=2, stride=2)


    def forward(self, x1):
        return self.up(x1)


class OutConv(nn.Module):
    def __init__(self, in_channels, out_channels):
        super(OutConv, self).__init__()
        self.conv = nn.Conv2d(in_channels, out_channels, kernel_size=1)

    def forward(self, x):
        return self.conv(x)


class Unet(nn.Module):
    def __init__(self, featureNet,n_classes=3):
        super(Unet, self).__init__()
        self.featureNet = featureNet
        self.up1 = Up(272,40)
        self.up2 = Up(80,24)
        self.up3 = Up(48,16)
        self.up4 = Up(32,16)
        self.up5 = UpwithoutConv(16,16)
        self.outc = OutConv(16, n_classes)
    def forward(self, x):
        feature_list = self.featureNet.get_feature(x)
        x = self.up1(feature_list.pop(), feature_list.pop())
        x = self.up2(x,feature_list.pop())
        x = self.up3(x,feature_list.pop())
        x = self.up4(x,feature_list.pop())
        x = self.up5(x)
        x = self.outc(x)
        return x
class PerceptualLoss(nn.Module):
    def __init__(self,net,perceptual_net, train=False):
        super(PerceptualLoss, self).__init__()
        self.net = net
        self.perceptual_net = perceptual_net
        self.criterion = nn.MSELoss().cuda()
        self.train = train
    def forward(self, x, y):
        la1,la2,la3,la4,la5 = self.perceptual_net.get_feature(y)
        out = self.net(x)
        out1,out2,out3,out4,out5, = self.perceptual_net.get_feature(out)
        loss = self.criterion(out1,la1)+self.criterion(out2,la2)+self.criterion(out3,la3)
        loss = self.criterion(out,y) + loss/3.
        return loss
    def get_net_parameters(self):
        return self.net.parameters()
    def get_perceptual_net_parameters(self):
        return self.perceptual_net.parameters()
